name = "Aryan"
nameshort = name[0:3]  # start from index 0 all the way till 3 (excluding 3)
print(nameshort)
character1 = name[1]
print(character1)


# for notes , here name[0:3] means start from index 0 all the way till 3 (excluding 3),in general name[start:end] means start from index start all the way till end (excluding end), and name[start:] means start from index start all the way till the end of the string, and name[:end] means start from index 0 all the way till end (excluding end).